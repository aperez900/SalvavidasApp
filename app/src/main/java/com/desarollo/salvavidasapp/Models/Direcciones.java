package com.desarollo.salvavidasapp.Models;

public class Direcciones {

    public String alias;
    public String primerTextoDireccion;
    public String segundoTextoDireccion;
    public String tercerTextoDireccion;
    public String cuartoTextoDireccion;
    public String quintoTextoDireccion;
    public String sextoTextoDireccion;
    public String municipio;
    public String barrio;
    public String adicional;



    public Direcciones(){

    }

    public String getAlias() {
        return alias;
    }

    public void setAlias(String alias) {
        this.alias = alias;
    }

    public String getPrimerTextoDireccion() {
        return primerTextoDireccion;
    }

    public void setPrimerTextoDireccion(String primerTextoDireccion) {
        this.primerTextoDireccion = primerTextoDireccion;
    }

    public String getSegundoTextoDireccion() {
        return segundoTextoDireccion;
    }

    public void setSegundoTextoDireccion(String segundoTextoDireccion) {
        this.segundoTextoDireccion = segundoTextoDireccion;
    }

    public String getTercerTextoDireccion() {
        return tercerTextoDireccion;
    }

    public void setTercerTextoDireccion(String tercerTextoDireccion) {
        this.tercerTextoDireccion = tercerTextoDireccion;
    }

    public String getCuartoTextoDireccion() {
        return cuartoTextoDireccion;
    }

    public void setCuartoTextoDireccion(String cuartoTextoDireccion) {
        this.cuartoTextoDireccion = cuartoTextoDireccion;
    }

    public String getQuintoTextoDireccion() {
        return quintoTextoDireccion;
    }

    public void setQuintoTextoDireccion(String quintoTextoDireccion) {
        this.quintoTextoDireccion = quintoTextoDireccion;
    }

    public String getSextoTextoDireccion() {
        return sextoTextoDireccion;
    }

    public void setSextoTextoDireccion(String sextoTextoDireccion) {
        this.sextoTextoDireccion = sextoTextoDireccion;
    }

    public String getMunicipio() {
        return municipio;
    }

    public void setMunicipio(String municipio) {
        this.municipio = municipio;
    }

    public String getBarrio() {
        return barrio;
    }

    public void setBarrio(String barrio) {
        this.barrio = barrio;
    }

    public String getAdicional() {
        return adicional;
    }

    public void setAdicional(String adicional) {
        this.adicional = adicional;
    }
}
